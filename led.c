//==============================================================================
void led_write(uint8_t led, uint16_t red, uint16_t green, uint16_t blue)
{

	switch(led)
	{
		case 1:
		unpacked[15] = red;
		unpacked[14] = green;
		unpacked[13] = blue;
		break;
		
		case 2:
		unpacked[12] = red;
		unpacked[11] = green;
		unpacked[10] = blue;
		break;
		
		case 3:
		unpacked[9] = red;
		unpacked[8] = green;
		unpacked[7] = blue;
		break;
		
		case 4:
		unpacked[6] = red;
		unpacked[5] = green;
		unpacked[4] = blue;
		break;
		
		case 5:
		unpacked[3] = red;
		unpacked[2] = green;
		unpacked[1] = blue;
		break;
	}
	pack12to8(unpacked, packed_stuff, sizeof(packed_stuff));
}

//------------------------------------------------------------------------------
//      __   __              ___  ___
//     |__) |__) | \  /  /\   |  |__
//     |    |  \ |  \/  /~~\  |  |___
//
//------------------------------------------------------------------------------

static void pack12to8(uint16_t *input, uint8_t *output, uint16_t count)
{
	// count = number of 12-bit samples in input[]
	uint16_t j = 0;
	for (uint16_t i = 0; i < count; i += 2)
	{
		uint16_t a = input[i] & 0x0FFF;        // 12 bits
		uint16_t b = input[i + 1] & 0x0FFF;

		output[j++] = a >> 4;                  // high 8 bits of a
		output[j++] = ((a & 0x000F) << 4) + (b >> 8); // low 4 of a + high 4 of b
		output[j++] = b & 0x00FF;              // low 8 of b
	}
}

