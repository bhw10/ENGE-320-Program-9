void led_init()
{
	// Configure the Arduino LED
	REG_PORT_DIR0 |= PORT_PA17;
	REG_PORT_OUTSET0 = PORT_PA17;
}
//==============================================================================
void led_write(uint8_t led, uint16_t red, uint16_t green, uint16_t blue)
{

	switch(led)
	{
		case 1:
		unpacked[15] = red;
		unpacked[14] = green;
		unpacked[13] = blue;
		break;
		
		case 2:
		unpacked[12] = red;
		unpacked[11] = green;
		unpacked[10] = blue;
		break;
		
		case 3:
		unpacked[9] = red;
		unpacked[8] = green;
		unpacked[7] = blue;
		break;
		
		case 4:
		unpacked[6] = red;
		unpacked[5] = green;
		unpacked[4] = blue;
		break;
		
		case 5:
		unpacked[3] = red;
		unpacked[2] = green;
		unpacked[1] = blue;
		break;
	}
	pack12to8(unpacked, packed_stuff, 16);
}

//==============================================================================

void led_writeAll(uint16_t red, uint16_t green, uint16_t blue)
{
	// Assign red LEDs
	unpacked[15] = red;
	unpacked[12] = red;
	unpacked[9] = red;
	unpacked[6] = red;
	unpacked[3] = red;
	
	// Assign green LEDs
	unpacked[14] = green;
	unpacked[11] = green;
	unpacked[8] = green;
	unpacked[5] = green;
	unpacked[2] = green;
	
	// Assign blue LEDs
	unpacked[13] = blue;
	unpacked[10] = blue;
	unpacked[7] = blue;
	unpacked[4] = blue;
	unpacked[1] = blue;
	
	pack12to8(unpacked, packed_stuff, 16);
}

//------------------------------------------------------------------------------
//      __   __              ___  ___
//     |__) |__) | \  /  /\   |  |__
//     |    |  \ |  \/  /~~\  |  |___
//
//------------------------------------------------------------------------------

static void pack12to8(uint16_t *input, uint8_t *output, uint16_t count)
{
	// count = number of 12-bit samples in input[]
	uint16_t j = 0;
	for (uint16_t i = 0; i < count; i += 2)
	{
		output[j++] = (input[i] >> 4) & 0xFF; // high byte of first element
		output[j++] = ((input[i] << 4) & 0xF0) |(( input[i+1] >> 8) & 0xFF); // low 4 of first element, high 4 of second element
		output[j++] = input[i + 1] & 0x00FF;              // low byte of second element
	}
}

